function generateProductName(brandName, categoryName) {
    const adjectives = ['Premium', 'Smart', 'Ultra', 'Pro', 'Classic', 'Stylish', 'Durable', 'Compact', 'Elegant', 'Advanced'];
    const nouns = {
        'Electronics': ['Gadget', 'Device', 'Tech', 'System'],
        'Fashion & Apparel': ['Wear', 'Style', 'Attire', 'Apparel'],
        'Home & Kitchen': ['Utility', 'Appliance', 'Decor', 'Cookware'],
        'Beauty & Personal Care': ['Essential', 'Solution', 'Elixir', 'Grooming'],
        'Sports & Outdoors': ['Gear', 'Equipment', 'Accessory', 'Tool'],
        'Books, Movies & Music': ['Collection', 'Media', 'Entertainment', 'System'],
        'Toys & Games': ['Fun', 'Play', 'Kit', 'Game', 'Toy'],
        'Baby & Kids': ['Comfort', 'Care', 'Accessory', 'Gear'],
        'Automotive & Tools': ['Part', 'Kit', 'Tool', 'Accessory'],
        'Health & Wellness': ['Support', 'Aid', 'Booster', 'Kit', 'Moisturizer'],
        'Pet Supplies': ['Care', 'Treat', 'Accessory', 'Food'],
        'Office & Stationery': ['Solution', 'Supply', 'Tool', 'Printer'],
        'Security & Protection': ['System', 'Guard', 'Solution'],
        'Lingerie & Loungewear': ['Comfort', 'Piece', 'Wear'],
        'Special Occasion Costume': ['Outfit', 'Ensemble', 'Dress-up']
    };
    const mainNoun = getRandomItem(nouns[categoryName] || ['Product']);
    return `${getRandomItem(adjectives)} ${brandName} ${mainNoun}`;
}

function createProduct() {
    const id = `prod${String(productIdCounter++).padStart(3, '0')}`;
    const categoryObj = getRandomItem(CATEGORY_DATA);
    const category = categoryObj.name;
    const subCategory = getRandomItem(categoryObj.subCategories);
    const brand = getRandomItem(TEMP_BRANDS_FOR_GENERATION); // Use temporary brands for generation
    const name = generateProductName(brand, category);
    const basePrice = getRandomNumber(500, 150000); // Wide range for prices
    const isDiscounted = Math.random() < 0.3; // 30% chance of discount
    const price = isDiscounted ? getRandomNumber(basePrice * 0.7, basePrice * 0.9, 2) : basePrice;
    const originalPrice = isDiscounted ? basePrice : null;
    const numImages = getRandomNumber(2, 6); // Random number of secondary images
    const images = [
        `images/products/${name.toLowerCase().replace(/ /g, '-').replace(/[^a-z0-9-]/g, '')}.webp`, // Product specific main image
        ...Array.from({ length: numImages }, (_, i) => `images/products/image${getRandomNumber(1, 6)}.webp`) // Generic secondary images
    ];
    const rating = getRandomNumber(3.0, 5.0, 1); // Rating between 3.0 and 5.0 with one decimal
    const reviewsCount = getRandomNumber(10, 500);
    const inStock = getRandomNumber(0, 200); // Can be 0 for out of stock

    const availableColors = Array.from({ length: getRandomNumber(2, 5) }, () => getRandomItem(COMMON_COLORS));
    const defaultColor = getRandomItem(availableColors);

    const description = `Discover the ${name}, a ${category} essential from ${brand}. This product boasts ${getRandomNumber(2,4)} unique features and a sleek design. Perfect for ${categoryObj.displayName.toLowerCase().replace(/ & /g, ' and ')} enthusiasts.`;

    const features = Array.from({ length: getRandomNumber(2, 5) }, () => getRandomItem(COMMON_FEATURES));

    return {
        id: id,
        name: name,
        description: description,
        category: category,
        subCategory: subCategory,
        brand: brand,
        price: parseFloat(price.toFixed(2)),
        originalPrice: originalPrice ? parseFloat(originalPrice.toFixed(2)) : null,
        isDiscounted: isDiscounted,
        images: images,
        rating: rating,
        reviewsCount: reviewsCount,
        inStock: inStock,
        sku: `${brand.substring(0, 3).toUpperCase()}-${category.substring(0, 3).toUpperCase()}-${id.toUpperCase()}`,
        features: features,
        shipping: Math.random() < 0.8 ? 'local' : 'overseas', // 80% local shipping
        waitingPeriod: getRandomItem(['2-3 business days', '3-5 business days', '5-7 business days', '7-14 business days']),
        minOrderQuantity: getRandomNumber(1, 2),
        maxOrderQuantity: getRandomNumber(5, 20),
        availableColors: availableColors,
        color: defaultColor, // Default color for display
        storage: category === 'Electronics' && subCategory === 'Smartphones' ? getRandomItem(['64GB', '128GB', '256GB', '512GB']) : null, // Example category-specific field
        size: category === 'Fashion & Apparel' ? getRandomItem(['S', 'M', 'L', 'XL', 'XXL']) : null, // Example category-specific field
    };
}

